package com.vpavlov.console;

/**
 * Logging tag
 *
 * @author vpavlov
 */
public enum LogType {
    /**
     * Error tag
     */
    ERROR,

    /**
     * Warning tag
     */
    WARNING,

    /**
     * Info tag
     */
    INFO,

    /**
     * Header tag
     */
    HEADER,

    /**
     * Event tag
     */
    EVENT,

    /**
     * Help info tag
     */
    HELP_INFO,

    /**
     * Debug tag
     */
    DEBUG

}
